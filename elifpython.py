#!/usr/bin/env python3

count = 20 

if count < 0:
	message = "is less than 0"
	print(count, message)

elif count < 20: 
	message = "is less than 20"
	print(count, message)

elif count > 20: 
	message = "is greater than 20"
	print(count, message) 

elif count != 20:
	message = "NOT TRUE"
	print(count, message)

else:
	message = "TRUE"
	print(count, message)

 
 
